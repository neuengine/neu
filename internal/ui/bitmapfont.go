package ui

// BitmapFont is a stdlib-only, dependency-free 8x8 bitmap typeface (T-6D04.2).
// The x/image/font ADR rejected a third-party TTF rasterizer to preserve the
// engine's zero-dependency property (C-003); this embedded bitmap font is the
// chosen path. Each glyph is authored as 8 lines of ASCII art ('#' = a lit
// pixel, anything else = off) so the source is human-verifiable — you can read
// the letter shape directly — and parsed to a [8]uint8 bit grid at init.
//
// Coverage is space + digits + uppercase A–Z + the punctuation the diagnostic
// overlay needs (its primary consumer renders e.g. "FPS: 60"). Lowercase and the
// remaining ASCII are a mechanical data-entry follow-up; an unmapped rune falls
// back to the missing-glyph box so layout never breaks.
type BitmapFont struct {
	glyphs  map[rune][8]uint8
	missing [8]uint8
	cell    uint8 // square cell size in source pixels (8)
}

// DefaultBitmapFont returns the built-in 8x8 font.
func DefaultBitmapFont() *BitmapFont {
	f := &BitmapFont{glyphs: make(map[rune][8]uint8, len(glyphArt)), cell: 8}
	for r, art := range glyphArt {
		f.glyphs[r] = parseGlyph(art)
	}
	f.missing = parseGlyph(missingGlyphArt)
	return f
}

// CellSize returns the font's square cell size in source pixels.
func (f *BitmapFont) CellSize() uint8 { return f.cell }

// Glyph returns the rune's bit grid and ok=true if the font defines it; for an
// undefined rune it returns the missing-glyph box and ok=false (so callers can
// distinguish a real glyph from the fallback while still getting a drawable box).
func (f *BitmapFont) Glyph(r rune) (rows [8]uint8, ok bool) {
	if g, defined := f.glyphs[r]; defined {
		return g, true
	}
	return f.missing, false
}

// Advance returns the rune's horizontal advance in source pixels. The font is
// monospace, so every printable glyph (including a space) advances one cell; a
// control rune (e.g. newline) advances nothing.
func (f *BitmapFont) Advance(r rune) uint8 {
	if r == '\n' || r == '\r' || r == '\t' {
		return 0
	}
	return f.cell
}

// parseGlyph converts 8 lines of ASCII art into a row-major bit grid: bit 7 of
// each byte is the left-most pixel. Short lines are padded with off-pixels;
// extra lines beyond 8 are ignored, so a malformed entry degrades to blank
// rather than panicking.
func parseGlyph(art [8]string) [8]uint8 {
	var rows [8]uint8
	for y := range 8 {
		line := art[y]
		var bits uint8
		for x := range 8 {
			if x < len(line) && line[x] == '#' {
				bits |= 1 << (7 - x)
			}
		}
		rows[y] = bits
	}
	return rows
}

